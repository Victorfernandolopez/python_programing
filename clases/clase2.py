#operadores aritmeticos
#suma
print(5 + 7)

#resta
print(5 - 7)

#multiplicacion
print(5 * 7)

#division
print(5 / 7)

#modulo
print(5 % 7)

#exponente
print(5 ** 2)


#operadores de comparacion

#igualdad
print(5 == 7)

#diferencia
print(5 != 7)

#mayor que
print(5 > 7)

#menor que
print(5 < 7)

#mayor o igual que
print(5 >= 7)

#menor o igual que
print(5 <= 7)

